export interface CreateIconParams {
  name: IconName
  size?: number
  strokeWidth?: number
}

export const createIcon = (params: CreateIconParams): SVGElement =>
  createIconFromMarkup(ICON_PATHS[params.name], params)

export interface CreateIconFromMarkupOptions {
  size?: number
  strokeWidth?: number
}

export const createIconFromMarkup = (
  innerMarkup: string,
  { size = 16, strokeWidth = 2 }: CreateIconFromMarkupOptions = {},
): SVGElement => {
  const svg = document.createElementNS(SVG_NS, 'svg')
  svg.setAttribute('viewBox', '0 0 24 24')
  svg.setAttribute('width', String(size))
  svg.setAttribute('height', String(size))
  svg.setAttribute('fill', 'none')
  svg.setAttribute('stroke', 'currentColor')
  svg.setAttribute('stroke-width', String(strokeWidth))
  svg.setAttribute('stroke-linecap', 'round')
  svg.setAttribute('stroke-linejoin', 'round')
  svg.innerHTML = innerMarkup
  return svg
}
